    void saveLoginData(){
        
        try {
            FileWriter file = new FileWriter("loginInfo.txt",true);
            BufferedWriter b=new BufferedWriter(file);
            DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
            Date date = new Date();
   
            b.write("\n" + txtUser.getText() + "\t"+dateFormat.format(date));
            b.newLine();
            b.close();
            file.close();
   
   
  
    }catch (IOException e) {
        JOptionPane.showMessageDialog(null, e.getMessage(),"File Not Found!", JOptionPane.ERROR_MESSAGE);
        }
    catch (Exception e){
       JOptionPane.showMessageDialog(null, e.getMessage(),"Error Found!", JOptionPane.ERROR_MESSAGE);
    }
    }


    
    
    
