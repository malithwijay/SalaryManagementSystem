    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
            
      
        try {
            Connection con= Database.createConnection();
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT UserName,Password FROM users");
   
            if (txtUser.getText().isEmpty()) {
        
                        JOptionPane.showMessageDialog(this, "Please Enter Username!","Warning",JOptionPane.WARNING_MESSAGE);
                }
            else if (txtPass.getText().isEmpty()) {
        
                        JOptionPane.showMessageDialog(this, "Please Enter Password!","Warning",JOptionPane.WARNING_MESSAGE);
                }
            else{
             while (rs.next()) {
                String UserName = rs.getString("UserName");
                String UserPassword = rs.getString("Password");
                
                if (txtUser.getText().equals(UserName)&& txtPass.getText().equals(UserPassword)) {
                        this.dispose();
                        new Dashboard().setVisible(true);
                        saveLoginData();
                        JOptionPane.showMessageDialog(this, "Login Data Saved!","INFO",JOptionPane.INFORMATION_MESSAGE );
                }
                else{
                    LblLoginError.setText("UserName Or Password Incorrect");
                }
               
              } 
            }
             
          } 
         catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"Error!", JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_btnLoginActionPerformed

